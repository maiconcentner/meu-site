// EVENT LISTENERS
// ============================================

document.addEventListener('DOMContentLoaded', () => {
  const navButtons = document.querySelectorAll('.nav-btn');
  const searchInput = document.getElementById('searchInput');

  // Renderizar aula 1 ao carregar
  renderLesson(1);

  // Click nos botões de navegação
  navButtons.forEach(btn => {
    btn.addEventListener('click', () => {
      const lessonNumber = parseInt(btn.dataset.lesson);
      
      // Remover active de todos
      navButtons.forEach(b => b.classList.remove('active'));
      
      // Adicionar active no clicado
      btn.classList.add('active');
      
      // Renderizar conteúdo
      renderLesson(lessonNumber);
    });
  });

  // Busca
  searchInput.addEventListener('input', (e) => {
    const searchTerm = e.target.value.toLowerCase();
    const weeks = document.querySelectorAll('.nav-week');
    
    weeks.forEach(week => {
      const buttons = week.querySelectorAll('.nav-btn');
      let weekHasMatch = false;
      
      buttons.forEach(btn => {
        const text = btn.textContent.toLowerCase();
        if (text.includes(searchTerm)) {
          btn.style.display = 'block';
          weekHasMatch = true;
        } else {
          btn.style.display = searchTerm ? 'none' : 'block';
        }
      });
      
      week.style.display = weekHasMatch || !searchTerm ? 'block' : 'none';
    });
  });
});
